def main():
    # Запрашиваем количество заявок
    print("Введите количество заявок: ")
    line = input().split()
    if not line:
        return
    N = int(line[0])

    # Список для хранения времени начала и конца каждой лекции
    lectures = []
    for i in range(N):
        # Считываем два числа: начало и конец
        start, end = map(int, input().split())
        lectures.append([start, end])

    # Сортируем все лекции по времени их окончания (второй элемент списка)
    # Это ключевой момент: чем раньше лекция закончится, тем больше места останется для других
    lectures.sort(key=lambda x: x[1])

    # Переменная для подсчета количества лекций
    count = 0
    # Переменная для хранения времени окончания последней выбранной лекции
    last_end_time = 0

    # Проходим по всем лекциям из отсортированного списка
    for lecture in lectures:
        start = lecture[0]
        end = lecture[1]

        # Если текущая лекция начинается не раньше, чем закончилась предыдущая
        if start >= last_end_time:
            # Мы можем провести эту лекцию
            count = count + 1
            # Обновляем время, когда аудитория освободится
            last_end_time = end

    # Выводим итоговый результат
    print("Максимальное количество заявок, которые можно выполнить:", count)

if __name__ == "__main__":
    main()
