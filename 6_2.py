import heapq

def main():
    # Считываем количество городов
    line1 = input().split()
    if not line1: return
    N = int(line1[0])

    # Считываем цены на бензин в каждом городе
    prices = list(map(int, input().split()))

    # Считываем количество дорог
    M = int(input())

    # Создаем пустой список смежности для графа
    # Индекс — это номер города, значение — список его соседей
    graph = []
    for i in range(N):
        graph.append([])

    # Заполняем граф дорогами
    for i in range(M):
        u, v = map(int, input().split())
        # Уменьшаем на 1, так как в программировании счет идет от 0
        u -= 1
        v -= 1
        
        # Цена поездки — это цена бензина в городе, из которого мы выезжаем
        graph[u].append((v, prices[u]))
        graph[v].append((u, prices[v]))

    # Вместо float('inf') используем очень большое число (больше любого возможного пути)
    infinity = 10**9
    
    # Список минимальных стоимостей до каждого города
    distances = [infinity] * N
    distances[0] = 0 # В начальном городе мы уже находимся, цена 0

    # Очередь с приоритетом для выбора самого "дешевого" пути на каждом шаге
    # Хранит пары: (текущая_цена, номер_города)
    pq = [(0, 0)]

    while len(pq) > 0:
        # Берем город с минимальной накопленной стоимостью
        current_cost, u = heapq.heappop(pq)

        # Если мы уже нашли путь дешевле, этот вариант пропускаем
        if current_cost > distances[u]:
            continue

        # Проверяем всех соседей текущего города
        for neighbor in graph[u]:
            v = neighbor[0]
            weight = neighbor[1]

            # Считаем, сколько будет стоить доехать до соседа через текущий город
            new_cost = distances[u] + weight

            # Если этот путь дешевле всех найденных ранее
            if new_cost < distances[v]:
                distances[v] = new_cost
                heapq.heappush(pq, (new_cost, v))

    # Выводим результат. Если число осталось огромным — пути нет.
    if distances[N-1] == infinity:
        print("Минимальное количество бензина на путь = -1")
    else:
        print("Минимальное количество бензина на путь =", distances[N-1])

main()

